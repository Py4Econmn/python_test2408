def two_times(input):
    """This function takes an input and returns 
        the input multiplied by 2
    """
    result = input * 2
    return result

def mult(input1, input2):
    result = input1 * input2
    return result

def mult2(input1, input2):
    result = input1 * input2
    return result, input1, input2
